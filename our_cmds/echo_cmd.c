#include "../minishell.h"

void    echo_cmd(char **cmd)
{
    int i = 1;
    int n_line = 0;
    
    if (!ft_strcmp(cmd[1], "-n"))
    {
        n_line = 1;
        i++;
    }
    while(cmd[i])
    {
        ft_putstr_fd(cmd[i++], 1);
        write(1, " ",1);
    }
    if(!n_line)
        printf("\n");
}